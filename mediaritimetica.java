package ti;
import java.util.Scanner;

public class mediaritimetica {
	


	    public static void main(String[] args) {
	        // mostra a mensagem
	        System.out.println("A média aritmética é: " + calculaMedia(6, 3, 69));
	    }

	    // faz o calculo da media e mostra o resultado
	    public static double calculaMedia(double a, double b, double c) {
	        
	        return (a + b + c) / 3;
	    }
	}



